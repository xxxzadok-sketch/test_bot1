    # –ü–æ–ª—É—á–∞–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –∑–∞–ø—Ä–æ—Å–æ–≤
    requests = db.get_pending_requests()
    pending_requests_count = len(requests)

    message = (
        f"üìä –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ —Å–∏—Å—Ç–µ–º—ã:\n\n"
        f"üë• –í—Å–µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π: {total_users}\n"
        f"üí∞ –í—Å–µ–≥–æ –±–æ–Ω—É—Å–Ω—ã—Ö –±–∞–ª–ª–æ–≤: {total_bonuses}\n"
        f"üèÜ –°—Ä–µ–¥–Ω–∏–π –±–∞–ª–∞–Ω—Å: {total_bonuses // total_users if total_users > 0 else 0} –±–∞–ª–ª–æ–≤\n\n"
        f"üìÖ –ë—Ä–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏—è:\n"
        f"‚è≥ –û–∂–∏–¥–∞—é—â–∏–µ: {booking_stats.get('pending', 0)}\n"
        f"‚úÖ –ü–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–Ω—ã–µ: {booking_stats.get('confirmed', 0)}\n"
        f"‚ùå –û—Ç–º–µ–Ω–µ–Ω–Ω—ã–µ: {booking_stats.get('cancelled', 0)}\n\n"
        f"üìã –ó–∞–ø—Ä–æ—Å—ã –Ω–∞ —Å–ø–∏—Å–∞–Ω–∏–µ: {pending_requests_count}"
    )

    # –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ - –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
